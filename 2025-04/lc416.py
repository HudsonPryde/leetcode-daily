from typing import List


class Solution:
    def canPartition(self, nums: List[int]) -> bool:
        n = len(nums)
        t = sum(nums)
        if t%2 == 1: return False
        target = t//2
        dp = [False]*(target+1)
        dp[0] = True
        
        for num in nums:
            for i in range(target,num-1,-1):
                dp[i] |= dp[i-num]
                
        return dp[-1]



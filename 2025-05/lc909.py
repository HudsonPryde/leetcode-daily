from typing import List
from collections import deque

class Solution:
    def snakesAndLadders(self, board: List[List[int]]) -> int:
        n = len(board)
        target = n**2
        d = deque([(0,0)])
        arr = []
        for i,x in enumerate(board[::-1]):
            if i%2==0:
                arr += x
            else:
                arr += x[::-1]
        
        visited = {}
        
        while d:
            cell, roll = d.popleft()
            if cell == target-1:
                return roll
            for i in range(cell+1, cell+7):
                if i < len(arr):
                    h = i if arr[i] == -1 else arr[i]-1
                    if h in visited and visited[h] <= roll+1: continue
                    visited[h] = roll
                    if arr[i] == -1:
                        d.append((i,roll+1))
                    else:
                        d.append((arr[i]-1,roll+1))
        return -1

